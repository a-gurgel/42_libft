#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*ptr;
	size_t	s_len;

	s_len = ft_strlen(s) + 1;
	ptr = malloc(s_len * sizeof(char));
	if (! ptr)
		return (NULL);
	return (ft_memcpy(ptr, s, s_len));
}
